package com.coherentsolutions.java.webauto.section07;

/**
 * Demonstrates the basic usage of the 'if' statement in Java.
 * This class evaluates a simple condition and executes code block if the condition is true.
 */
public class IfStatementExample {
    public static void main(String[] args) {
        int x = 10;

        if (x < 20) {
            System.out.print("This is an if statement");
        }
    }
}
