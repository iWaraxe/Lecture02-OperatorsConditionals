package com.coherentsolutions.java.webauto.section07;

/**
 * Demonstrates the usage of 'if-else' statement in Java.
 * This class evaluates a condition and executes different blocks of code based on whether
 * the condition is true or false.
 */
public class IfElseStatementExample {
    public static void main(String[] args) {
        int temperature = 25;

        if (temperature > 30) {
            System.out.println("It's a hot day.");
        } else {
            System.out.println("It's not so hot.");
        }
    }
}
